            <p>If you are considering filing bankruptcy, it is likely that your phone has already been inundated
                with calls from creditors who will stop at nothing to make sure they are paid. They may be rude,
                or even threatening to you over the phone. The Fair Debt Collection Practices Act was passed in
                1977 to protect consumers from harassment and other abuses inflicted by debt collectors. In the
                following lesson we will learn about several different types of laws, but pay close attention to
                the Fair Debt Collection Practices Act, because it may help you whether you decide to file
                bankruptcy or not.</p>
            <h3>Fair Debt Collection Practices Act (FDCPA)</h3>
            <p>The first thing to keep in mind is that the FDCPA does not apply to the original creditor. They
                have a lot more leeway to call you excessively or do other things to convince you to pay. Once
                that debt is sold off to another third-party debt collector though, the FDCPA applies to that
                third-party. <a target="_blank"
                    href="https://www.thebalance.com/debt-collector-fdcpa-violations-960603">Here</a> is a
                list of 15 things debt collectors cannot do under the FDCPA. The important thing is to document
                everything. You want to keep detailed records as to when the call took place and what was said.
                You may even want to tape-record the conversation (provided doing so is legal in your
                jurisdiction).</p>
            <p>One of the interesting things the FDCPA lets you do is to tell the creditor to stop contacting
                you in regards to the debt. It stops their phone calls, period. This has to be done in writing,
                so you will have to send a letter. Once they receive this letter, they are prohibited from
                contacting you further. However, keep in mind that this only works for the debt collector that
                you send the letter to.</p>
            <p>For example, lets say you owe $10,000 on your Visa Card. Visa turns around and sells the debt to
                Joe Smoe Collection Company, and you write a letter to Joe Smoe telling them to cease
                communication. They will cease communication, but if Joe Smoe turns around and sells the debt to
                Persistant Collections, Persistant Collections will not be prohibited from contacting you. For
                an example of one of these letters that you can use yourself, go <a target="_blank"
                    href="https://www.consumerfinance.gov/ask-cfpb/what-should-i-do-when-a-debt-collector-contacts-me-en-1695/">here</a>.
            </p>
            <p>Debtors are fighting back against slimy collection company practices, by recovering money from
                these companies when they violate the FDCPA. We strongly recommend that if you believe a
                collection agency has violated the FDCPA that you talk to an attorney who can discuss your
                rights. Not all bankruptcy attorneys have experience with the FDCPA as it is a pretty
                specialized area of the law, but they may offer a good recommendation as to who you could talk
                to.</p>
            <p>Although we do not recommend going at it alone, it is possible to take the debt collectors to
                court yourself. <a target="_blank" href="https://creditboards.com/">CreditBoards</a> is a
                message forum full of information submitted by other debtors on how to sue under the FDCPA and
                win.</p>
            <p>You can read the full text of the FDCPA <a target="_blank"
                    href="https://www.fdic.gov/regulations/laws/rules/6500-1100.html">here</a>.</p>
            <h3>Truth in Lending Act</h3>
            <p>The Truth in Lending Act requires credit card issuers inform consumers about the APR (Annual
                Percentage Rate), the method of finance charges, and all other terms and conditions. It helps
                consumers shop around for the best places to get credit.</p>
            <p>If you are shopping around for a new Credit Card, we highly recommend that you check out a credit
                card comparison tool on a website like <a target="_blank"
                    href="https://www.nerdwallet.com/credit-cards">NerdWallet</a>. While you may not have any
                use for it currently, it may be a helpful resource for you in the future as you take steps to
                rebuild your credit.</p>
            <p>You can read the full text of the Truth in Lending Act <a target="_blank"
                    href="https://www.fdic.gov/regulations/laws/rules/6500-200.html">here</a>.</p>
            <h3>Fair Credit Billing Act</h3>
            <p>The Fair Credit Billing Act gives the consumer 60 days to dispute any bill that they receive. The
                consumer must do this in writing and it must be sent to the customer service department of the
                creditor. The consumer should be careful to include their account number, their name, and an
                explanation of why they feel the bill is incorrect. Any supporting documentation which may
                helpful should be included, and a copy of everything sent in should be retained for your
                records.</p>
            <p>The creditor then has 30 days in which to respond, and the creditor cannot report any delinquency
                to a credit reporting agency until the matter is settled. The creditor can continue to add
                finance charges to the principle balance, however. The law also allows consumers to dispute the
                charge for defective goods or services bought with a credit card.</p>
            <p>You can read the full text of the Fair Billing Act <a target="_blank"
                    href="https://www.fdic.gov/regulations/laws/rules/6500-200.html">here</a>.</p>
            <h3>Equal Credit Opportunity Act (ECOA)</h3>
            <p>The Equal Credit Opportunity Act (ECOA), just as it sounds provides protections to make sure that
                consumers have an equal opportunity to get credit, and it also dictates what factors lenders can
                look at when deciding whether to extend credit. For example, lenders can look at things like the
                amount of debt you have, your income and expenses, and your credit history, but they cannot,
                however, deny credit based on religion, race, sex, job rating, marital status, pregnancy,
                alimony or child support, age, or spousal income. Various states also prohibit discrimination
                based on sexual orientation or mental and physical disability.</p>
            <p>You can read the full text of the Equal Credit Opportunity Act <a target="_blank"
                    href="https://www.fdic.gov/regulations/laws/rules/6500-200.html">here</a>.</p>
            <h3>Fair Credit Reporting Act</h3>
            <p>The Fair Credit Reporting Act prohibits anyone (it could result in fines or imprisonment) from
                looking at your credit report for any reason other than the following:</p>
            <ul>
                <li>Application for business or government licenses</li>
                <li>Credit or insurance</li>
                <li>Court subpoena from a federal grand jury</li>
                <li>Employment</li>
                <li>Child support requirements</li>
            </ul>
            <p>Within 60 days of denied credit, insurance, employment, or housing, the consumer can request a
                free copy of their report. By law, the consumer is entitled to dispute and correct their credit
                report under this act.</p>
            <h3>Servicemembers Civil Relief Act (SCRA)</h3>
            <p>The SCRA provides protections for active military personnel from foreclosures, evictions,
                lawsuits, and debt collection. It may also allow lowering of interest rates on certain debts.
            </p>
            <p>The important thing to remember is if you are in the military and you are filing bankruptcy, it
                is important to mention this fact to your bankruptcy attorney. They can explain your rights and
                increased options under this law.</p>
            <h3>Uniformed Services Employment and Reemployment Rights Act (USERRA)</h3>
            <p>This act applies to all employers and protects the jobs and benefits of active duty military
                personnel for up to 5 years. It basically allows those personnel to return to their place of
                employment as if they had never left, with the same status and right to be treated the same as
                if they had never left. The U.S. Department of Labor is responsible for handling any employer
                violations of this act. They can be reached at (518) 457-7465.</p>



